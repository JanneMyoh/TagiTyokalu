    public void addData(HashMap<String,String> data, String id)
    {
        idArvo = data.get(id);
        GridBagConstraints c = new GridBagConstraints();
        c.fill = GridBagConstraints.HORIZONTAL;
        c.anchor = GridBagConstraints.LINE_START;
        c.ipadx = 20;
        //haetaan datamapin avaimet
        Set<String> tmpAvaimet = data.keySet();
        String[] avaimet = tmpAvaimet.toArray(new String[tmpAvaimet.size()]);
        int i = 0;
        for(String avain : avaimet)
        {
            //lis채t채채n sille labeli ja arvo kent채t
            
            c.gridx = 0;
            c.gridy = i;
            this.add(new JLabel(avain),c);
            c.gridx = 1;
            c.gridy = i;
            this.add(new JLabel(data.get(avain)),c);
            i++;
        }
    }

